// MENSAJE INICIAL
function mostrarMensajeInicial() {
  const hoy = new Date();
  const clave = String(hoy.getMonth() + 1).padStart(2, '0') + "-" + String(hoy.getDate()).padStart(2, '0');

  if (mensajesEspeciales[clave]) {
    mensajeInicial.textContent = mensajesEspeciales[clave];
  } else {
    const random = mensajesIniciales[Math.floor(Math.random() * mensajesIniciales.length)];
    mensajeInicial.textContent = random;
  }
}

// NOTAS
function mostrarNota() {
  const categoria = document.getElementById("categoria").value;
  const nota = document.getElementById("nota");
  const player = document.getElementById("player");

  const lista = contenido[categoria];
  const random = lista[Math.floor(Math.random() * lista.length)];

  nota.style.opacity = 0;

  setTimeout(() => {
    if (categoria === "audios") {
      nota.textContent = random.texto;
      player.src = random.src;
      player.style.display = "block";
    } else {
      nota.textContent = random;
      player.style.display = "none";
    }
    nota.style.opacity = 1;
  }, 150);
}

